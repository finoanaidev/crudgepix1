from rest_framework import viewsets
from rest_framework.response import Response
from rest_framework import status
from api.models import Syndic, Copropriete, Lot, Coproprietaire, DocumentCopro, Document
from api.serializer import SyndicSerializer, CoproprieteSerializer, LotSerializer, CoproprietaireSerializer, DocumentCoproSerializer,DocumentSerializer

class SyndicViewSet(viewsets.ModelViewSet):
    queryset = Syndic.objects.all()
    serializer_class = SyndicSerializer

    def update(self, request, *args, **kwargs):
        instance = self.get_object()
        serializer = self.get_serializer(instance, data=request.data)
        serializer.is_valid(raise_exception=True)
        serializer.save()
        return Response(serializer.data)

    def destroy(self, request, *args, **kwargs):
        instance = self.get_object()
        self.perform_destroy(instance)
        return Response(status=status.HTTP_204_NO_CONTENT)

